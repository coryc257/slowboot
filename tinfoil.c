{
	printk(KERN_INFO "Beginning SlowBoot\n");
	
	tinfoil.failures = 0;
	tinfoil.st = NULL;
	tinfoil.st = (struct kstat *)kmalloc(sizeof(struct kstat), GFP_KERNEL);
	if (!tinfoil.st)
		return -ENOMEM; // CHECK
	
	slowboot_run_test();
	kfree(tinfoil.st);
	return 0;
}

